        <h1>Balanceo de ecuaciones o balanceo por "tanteo"</h1>
        <p>Balanceo es sinónimo de <strong>equilibrio.</strong> Su objetivo es
             igualar el número de átomos de cada uno de los 
             elementos antes y después de la reacción. Para 
             ellos se utilizarán coeficientes que se colocarán 
             delante de la fórmula química de cada materia. 
             <strong>Importante: No se debe alterar los subíndices de 
             las fórmulas, no incluir números en ella porque 
             se cambiaría totalmente la expresión original.</strong></p>
             <h2>Pasos para balancear una ecuación química:</h2>
             <ol>
            <li>Escribe la ecuación química, asegurándote de que esté
                correctamente escrita con los reactivos a la <strong>izquierda</strong>
                de la flecha y los productos a la <strong>derecha.</strong></li>
            <li>Identifica los elementos presentes en la ecuación y haz
                un recuento del número de átomos de cada elemento en los
                reactivos y en los productos.</li>
            <li>Comienza balanceando los elementos que aparecen una sola 
                vez en cada lado de la ecuación, utilizando coeficientes 
                estequiométricos (números que se colocan delante de las 
                fórmulas) para igualar el número de átomos de cada elemento.</li>
            <li>Continúa balanceando los elementos que aparecen en más de un 
                compuesto, ajustando los coeficientes hasta que el número de 
                átomos de cada elemento sea el mismo en ambos lados de la ecuación.</li>
            <li>Verifica que la ecuación balanceada tenga el mismo número total 
                de átomos de cada elemento en ambos lados.</li>
        </ol>
        <p><strong>Ejemplo:</strong> Na + O<sub>2</sub> = Na<sub>2</sub>O <br>
        <strong>Ecuación balanceada:</strong> 4Na + O<sub>2</sub> = 2Na<sub>2</sub>O</p>

        <h1>Reacciones Químicas</h1>
        <p>Las reacciones químicas son procesos en los cuales
            <strong>una o más sustancias</strong> (llamadas reactivos) se
            transforman
            en <strong>una o más sustancias diferentes</strong> (llamadas productos),
            mediante la ruptura y formación de enlaces químicos.
            Durante una reacción química, los átomos de los
            reactivos se reorganizan para formar nuevos compuestos
            con propiedades distintas a las de los compuestos
            iniciales. Las reacciones químicas pueden ser
            clasificadas en diferentes tipos, como: </p>
        <ul>
            <li><strong>Combinación:</strong> En este tipo de cambio
                químico, se une dos o mas especies químicas para
                producir un material con características
                diferentes a sus componentes.
                <br><strong>Ejemplo: A + B - AB</strong>
            </li>
            <li><strong>Desplazamiento:</strong> Se caracteriza por
            la sustitución de un elemento por otro dentro
            de un compuesto químico.
            <br><strong>Ejemplo: A + BC - AB + C</strong>
            </li>
            <li><strong>Descomposición:</strong> Aquí existe una
            ruptura o separación de los componentes de una
            especialidad química. Puede ser ocasionada por el
            calor o la electricidad.
            <br><strong>Ejemplo: AB - A + B</strong>
            </li>
            <li><strong>Doble descomposición:</strong> Consiste
            en la ruptura de especies químicas seguidas de un
            reordenamiento de los componentes. Se puede decir
            que es un intercambio de pareja.
            <br><strong>Ejemplo: AB + CD - AC + BD</strong>
            </li>
        </ul>
